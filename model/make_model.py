        if self.training:

            ### inital a-sketch
            sid = self.sketch_net_i(sx)
       
            ### updated a-sketch
            sxf = self.sketch_net(sx)
            sx = self.train_transforms((sxf.expand(sxf.size(0), 3, sxf.size(2), sxf.size(3)) + 1) / 2.0 * 255.0)
